public class postincrement{
    public static void main(String[] args) {
        int vnum=100;
        System.out.println(vnum);
        int post =vnum++;
        System.out.println(post);
        System.out.println(vnum);
        System.out.println(post++);
        System.out.println(post++);
        System.out.println(post++);
        System.out.println(post);
    }
}